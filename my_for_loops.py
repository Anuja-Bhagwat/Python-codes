#For Loops in Python.
#For loop is used for itering over sequence (the lists, tuples, dictionary,set, string)

# Simple For loop example
Fruits = ["Apple","Mangoes","Strawberries","Coconut","Banana"]  # Define a list named Fruits
for x in Fruits:   # Define x variable that will iterate every element from list
    print(x)

#Using if-else statement with for loop
String = " Welcome to Hello World"
for s in String:
    if(s=="o"):
        print("Found the character o")
    else:
        print(s)


