import math
min_number = min(15,35,65)  # min() function to get lowest number
max_number = max(67,88,45)  #max() function to get highest number
print(min_number)
print(max_number)

x = abs(-27.56)  # abs() function gives the absolute positive value
print(x)

x = pow(4,3)  #The pow(x, y) function returns the value of x to the power of y (xy).
print(x)

x = math.sqrt(64)  #The math.sqrt() method returns the square root of a number
print(x)

x = math.ceil(2.6)  #The math.ceil() method rounds a number upwards to its nearest integer  
y = math.floor(2.3) #the math.floor() method rounds a number downwards to its nearest integer
print(x)
print(y)

x = math.pi  #The math.pi constant, returns the value of PI (3.14...)
print(x)